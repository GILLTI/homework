package iftest;
// 6번 : 조건문 if~else
public class IfTest02 {
			public static void main(String[] args) {
				
				int num1 = 10, num2 = 20;
				System.out.println("1번");
				
				if(num1 > num2) {
						System.out.println("2");
				}
				else {
					System.out.println("3");
				}
				System.out.println("4");
			}
}
